# Нехай на кожну сходинку можна стати з попередньої або переступивши через одну. Визначте, скількома способами
# можна піднятися на задану сходинку.

step_0 = 1
step_1 = 1

N = int(input('Введіть кількість сходинок: '))
if N < 2:
    print('Піднятися на 1 сходинку можна одним способом')
else:
    for i in range(2, N+1):
        s_n = step_0 + step_1
        step_0 = step_1
        step_1 = s_n

print('Піднятися на', N, 'сходинок можна такою кількістю способів:', s_n)
