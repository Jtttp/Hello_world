print('Обчислення суми всіх натуральних чисел від a до b (включно), які є кратними середньому арифметичному проміжку')
a = int(input('Введіть число а: '))
b = int(input('Введіть число b, більше ніж а: '))
avr = int((a+b) / 2)
s = 0
for i in range(a,b+1):
    if i % avr == 0:
        s = s + 1
print('Результат: ', s)

