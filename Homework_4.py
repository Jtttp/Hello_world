print('Обчислення суми всіх натуральних чисел від a до b (включно)')
a = int(input('Введіть число а: '))
b = int(input('Введіть число b, більше ніж а: '))
s = 0
for i in range (a, b+1):
     s = s + i
print('Результат: ', s)

